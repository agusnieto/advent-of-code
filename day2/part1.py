# secuencia de digitos que se repite DOS VECES
# Invalidos
#   11, 99, 1010, 3333, 123123, 118855118855
# Validos
#   999, 0101 -> 101, 11111

def rangeScan(start, end):
    invalid_ids = []
    for i in range(start, end + 1):
        lenght = len(str(i))
        if lenght % 2 == 0:
            split = lenght // 2
            if str(i)[:split] == str(i)[split:]:
                invalid_ids.append(i)
    return invalid_ids

